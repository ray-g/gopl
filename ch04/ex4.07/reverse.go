package reverse

import "unicode/utf8"

func rev(b []byte) {
	size := len(b)
	for i := 0; i < len(b)/2; i++ {
		b[i], b[size-1-i] = b[size-1-i], b[i]
	}
}

func reverse(b []byte) {
	// Reverse each UTF-8 rune
	for i := 0; i < len(b); {
		_, size := utf8.DecodeRune(b[i:])
		rev(b[i : i+size])
		i += size
	}
	// Reverse the whole bytes holding Reversed-UTF-8
	rev(b)
}
